package IntroduçãoJava;

public class TipoChar {
    public static void main(String[] args) {


        char aviao[];
        aviao  =  new char[1];
        aviao[0] = 'a';

        char caneta[];
        caneta = new char[1];
        caneta[0] = 'c';

        char bola[];
        bola = new char[1];
        bola[0] = 'b';


        System.out.println(aviao[0]);
        System.out.println(caneta[0]);
        System.out.println(bola[0]);
    }

}
