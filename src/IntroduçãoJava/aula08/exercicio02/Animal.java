package IntroduçãoJava.aula08.exercicio02;

public abstract class Animal {
    private String nome;
    private String raca;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getRaça() {
        return raca;
    }

    public void setRaça(String raca) {
        this.raca = raca;
    }


}
